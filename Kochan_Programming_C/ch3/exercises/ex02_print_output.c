/*
 *      Exercise 3.2 from Kochan's "Programming in C"
 *
 *      Write a program that prints the following text at the terminal:
 *        - In C, lowercase letters are significant.
 *        - main is where program execution begins.
 *        - Opening and closes braces enclose program statements in routine.
 *        - All program statements must be terminated by a semicolon.
 */

#include <stdio.h>

int main (void)
{
        printf ("In C, lowercase letters are significant.\n");
        printf ("main is where program execution begins.\n");
        printf ("Opening and closes braces enclose program statements in routine.\n");
        printf ("All program statements must be terminated by a semicolon.\n");

        return 0;
}
